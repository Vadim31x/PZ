## 1. Дано целое число A. Проверить истинность высказывания: «Число A является
## нечетным».

# Вводим целое число A
try:
    A = int(input("Введите целое число A: "))

    # Проверяем, является ли A нечетным
    print(f"Число {A} является нечетным: {A % 2 != 0}")

except ValueError:
    print("Ошибка: Пожалуйста, введите корректное целое число.")  # Обработка нечислового ввода
